  INDEX idx_ia_keywords_keyword (keyword),
  UNIQUE KEY uk_causa_keyword (causa_id, keyword)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

